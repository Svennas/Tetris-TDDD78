package se.liu.ida.gussv907.tddd78.tetris;

/**
 * A class of Enum type. Contains all the types of squares that exists in the game.
 */
public enum SquareType
{
    /** The I block in tetris. */           I_BLOCK,
    /** The O block in tetris. */           O_BLOCK,
    /** The T block in tetris. */           T_BLOCK,
    /** The S block in tetris. */           S_BLOCK,
    /** The Z block in tetris. */           Z_BLOCK,
    /** The J block in tetris. */           J_BLOCK,
    /** The L block in tetris. */           L_BLOCK,
    /** Acts as a border block. */          OUTSIDE,
    /** The empty block. */                 EMPTY
}